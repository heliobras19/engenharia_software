    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Carro novoCarro = new Carro();
        novoCarro.marca = this.marca.getText();
       novoCarro.arCondicionado = this.ar_condicionado.getSelectedItem() == "Sim";
        novoCarro.combustivel = (String) this.combustivel.getSelectedItem();
        novoCarro.identificador = this.identificador.getText();
        novoCarro.modelo = this.modelo.getText();
        String texto = this.ano.getText();
        try {
           //novoCarro.numPorta = Integer.parseInt((String) this.numPortas.getValue());
            novoCarro.ano =  Integer.parseInt(texto);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Verifique o ano e o numero de portas precisam ser numeros");
        }
        this.inController.carros.add(novoCarro);
         JOptionPane.showMessageDialog(null, "Carro registrado com sucesso !");
         this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed


